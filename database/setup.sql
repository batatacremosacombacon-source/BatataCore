create or replace function private.is_admin()
returns boolean
language sql
stable
security definer
set search_path = public, pg_temp
as $$
    select exists (
        select 1
        from public.profiles p
        where p.id = (select auth.uid())
          and p.role in ('admin','organizador')
    );
$$;
revoke all on function private.is_admin() from public;
grant execute on function private.is_admin() to authenticated;

create or replace function private.touch_updated_at()
returns trigger
language plpgsql
set search_path = public, pg_temp
as $$
begin
    new.updated_at = now();
    return new;
end;
$$;

create or replace function private.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public, pg_temp
as $$
begin
    insert into public.profiles(id, full_name, email, role)
    values (
        new.id,
        coalesce(new.raw_user_meta_data ->> 'full_name', ''),
        coalesce(new.email, ''),
        'participante'
    )
    on conflict (id) do update
       set email = excluded.email,
           full_name = case when public.profiles.full_name = '' then excluded.full_name else public.profiles.full_name end;
    return new;
end;
$$;

create or replace function private.protect_profile_role()
returns trigger
language plpgsql
security definer
set search_path = public, pg_temp
as $$
begin
    if old.role is distinct from new.role
       and (select auth.uid()) is not null
       and not private.is_admin() then
        raise exception 'Apenas administradores podem alterar níveis de acesso.';
    end if;
    return new;
end;
$$;

create or replace function private.log_admin_action()
returns trigger
language plpgsql
security definer
set search_path = public, pg_temp
as $$
declare
    v_action text;
    v_id text;
begin
    if tg_op = 'INSERT' then
        v_action := 'insert';
        v_id := new.id::text;
    elsif tg_op = 'UPDATE' then
        v_action := 'update';
        v_id := new.id::text;
    else
        v_action := 'delete';
        v_id := old.id::text;
    end if;

    if (select auth.uid()) is not null then
        insert into public.audit_logs(actor_id, action, entity, entity_id)
        values ((select auth.uid()), v_action, tg_table_name, v_id);
    end if;

    if tg_op = 'DELETE' then
        return old;
    end if;
    return new;
end;
$$;

