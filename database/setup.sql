create or replace function public.criar_inscricao(
    p_evento_id bigint,
    p_nome text,
    p_email text,
    p_telefone text,
    p_cidade text,
    p_tipo text,
    p_observacao text default '',
    p_consentimento_lgpd boolean default false
)
returns table(id bigint, ticket_code text, status text)
language plpgsql
security definer
set search_path = public, pg_temp
as $$
declare
    v_evento public.eventos%rowtype;
    v_status text;
    v_id bigint;
    v_ticket text;
begin
    if not p_consentimento_lgpd then
        raise exception 'É necessário aceitar o tratamento de dados para concluir a inscrição.';
    end if;

    if length(trim(coalesce(p_nome, ''))) < 3
       or position('@' in coalesce(p_email, '')) < 2
       or length(regexp_replace(coalesce(p_telefone, ''), '\D', '', 'g')) < 10 then
        raise exception 'Dados de inscrição inválidos.';
    end if;

    select * into v_evento
    from public.eventos
    where eventos.id = p_evento_id
      and eventos.deleted_at is null
    for update;

    if not found then
        raise exception 'Evento não encontrado.';
    end if;

    if v_evento.status <> 'publicado' then
        raise exception 'Este evento não está aberto para inscrições.';
    end if;

    if v_evento.data_evento < current_date then
        raise exception 'Este evento já foi encerrado.';
    end if;

    if exists (
        select 1 from public.inscritos i
        where i.evento_id = p_evento_id
          and lower(i.email) = lower(trim(p_email))
          and i.status <> 'cancelado'
    ) then
        raise exception 'Já existe uma inscrição ativa para este e-mail neste evento.';
    end if;

    v_status := case
        when v_evento.vagas_ocupadas >= v_evento.vagas_total then 'lista_espera'
        else 'confirmado'
    end;

    insert into public.inscritos(
        user_id, evento_id, nome, email, telefone, cidade,
        evento, tipo, observacao, status, consentimento_lgpd
    ) values (
        (select auth.uid()), p_evento_id, trim(p_nome), lower(trim(p_email)),
        trim(p_telefone), trim(p_cidade), v_evento.nome, trim(p_tipo),
        trim(coalesce(p_observacao, '')), v_status, p_consentimento_lgpd
    ) returning inscritos.id, inscritos.ticket_code into v_id, v_ticket;

    if v_status = 'confirmado' then
        update public.eventos
           set vagas_ocupadas = least(vagas_total, vagas_ocupadas + 1)
         where eventos.id = p_evento_id;
    end if;

    insert into public.email_queue(recipient, template, payload)
    values (
        lower(trim(p_email)),
        'inscricao_confirmada',
        jsonb_build_object(
            'nome', trim(p_nome),
            'evento', v_evento.nome,
            'data_evento', v_evento.data_evento,
            'status', v_status,
            'ticket_code', v_ticket
        )
    );

    return query select v_id, v_ticket, v_status;
end;
$$;
revoke all on function public.criar_inscricao(bigint,text,text,text,text,text,text,boolean) from public;
grant execute on function public.criar_inscricao(bigint,text,text,text,text,text,text,boolean) to anon, authenticated;

create or replace function public.cancelar_inscricao(p_inscricao_id bigint)
returns boolean
language plpgsql
security definer
set search_path = public, pg_temp
as $$
declare
    v_inscricao public.inscritos%rowtype;
begin
    select * into v_inscricao
    from public.inscritos
    where inscritos.id = p_inscricao_id
    for update;

    if not found then
        raise exception 'Inscrição não encontrada.';
    end if;

    if not private.is_admin()
       and (select auth.uid()) is distinct from v_inscricao.user_id then
        raise exception 'Você não tem permissão para cancelar esta inscrição.';
    end if;

    if v_inscricao.checked_in_at is not null then
        raise exception 'Não é possível cancelar após o check-in.';
    end if;

    if v_inscricao.status = 'cancelado' then
        return true;
    end if;

    update public.inscritos
       set status = 'cancelado', cancelled_at = now()
     where id = p_inscricao_id;

    if v_inscricao.status = 'confirmado' and v_inscricao.evento_id is not null then
        update public.eventos
           set vagas_ocupadas = greatest(0, vagas_ocupadas - 1)
         where id = v_inscricao.evento_id;
    end if;

    return true;
end;
$$;
revoke all on function public.cancelar_inscricao(bigint) from public;
grant execute on function public.cancelar_inscricao(bigint) to authenticated;

create or replace function public.realizar_checkin(p_ticket_code text)
returns table(inscricao_id bigint, nome text, evento text, checked_in_at timestamptz)
language plpgsql
security definer
set search_path = public, pg_temp
as $$
declare
    v_row public.inscritos%rowtype;
begin
    if not private.is_admin() then
        raise exception 'Acesso administrativo necessário.';
    end if;

    select * into v_row
    from public.inscritos
    where lower(inscritos.ticket_code) = lower(trim(p_ticket_code))
    for update;

    if not found then
        raise exception 'Ingresso não encontrado.';
    end if;

    if v_row.status = 'cancelado' then
        raise exception 'Esta inscrição foi cancelada.';
    end if;

    if v_row.checked_in_at is null then
        update public.inscritos
           set checked_in_at = now(), status = 'confirmado'
         where id = v_row.id
         returning * into v_row;
    end if;

    return query select v_row.id, v_row.nome, v_row.evento, v_row.checked_in_at;
end;
$$;
revoke all on function public.realizar_checkin(text) from public;
grant execute on function public.realizar_checkin(text) to authenticated;

create or replace function public.gerar_certificado(p_inscricao_id bigint)
returns uuid
language plpgsql
security definer
set search_path = public, pg_temp
as $$
declare
    v_inscricao public.inscritos%rowtype;
    v_evento public.eventos%rowtype;
    v_codigo uuid;
begin
    select * into v_inscricao from public.inscritos where id = p_inscricao_id;
    if not found then raise exception 'Inscrição não encontrada.'; end if;

    if not private.is_admin()
       and (select auth.uid()) is distinct from v_inscricao.user_id then
        raise exception 'Sem permissão para gerar este certificado.';
    end if;

    if v_inscricao.checked_in_at is null then
        raise exception 'O certificado exige check-in confirmado.';
    end if;

    select * into v_evento from public.eventos where id = v_inscricao.evento_id;
    if not coalesce(v_evento.certificado_disponivel, false) then
        raise exception 'O certificado ainda não está disponível.';
    end if;

    insert into public.certificados(inscricao_id)
    values (p_inscricao_id)
    on conflict (inscricao_id) do update set inscricao_id = excluded.inscricao_id
    returning codigo into v_codigo;

    return v_codigo;
end;
$$;
revoke all on function public.gerar_certificado(bigint) from public;
grant execute on function public.gerar_certificado(bigint) to authenticated;

create or replace function public.validar_certificado(p_codigo uuid)
returns table(
    participante text,
    evento text,
    data_evento date,
    carga_horaria numeric,
    emitted_at timestamptz
)
language sql
stable
security definer
set search_path = public, pg_temp
as $$
    select i.nome, e.nome, e.data_evento, e.carga_horaria, c.emitted_at
      from public.certificados c
      join public.inscritos i on i.id = c.inscricao_id
      join public.eventos e on e.id = i.evento_id
     where c.codigo = p_codigo
       and i.checked_in_at is not null
     limit 1;
$$;
revoke all on function public.validar_certificado(uuid) from public;
grant execute on function public.validar_certificado(uuid) to anon, authenticated;

