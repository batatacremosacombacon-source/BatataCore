create table if not exists public.profiles (
    id uuid primary key references auth.users(id) on delete cascade,
    full_name text not null default '',
    email text not null default '',
    phone text,
    city text,
    role text not null default 'participante',
    avatar_url text,
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now(),
    constraint profiles_role_check check (role in ('admin','organizador','equipe','participante'))
);

create table if not exists public.eventos (
    id bigint generated by default as identity primary key,
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now(),
    owner_id uuid references auth.users(id) on delete set null,
    nome text not null,
    slug text not null unique,
    categoria text not null default 'Evento',
    data_evento date not null,
    hora_inicio time,
    hora_fim time,
    local text not null,
    endereco text,
    formato text not null default 'Presencial',
    vagas_total integer not null default 1,
    vagas_ocupadas integer not null default 0,
    descricao text not null default '',
    imagem_url text,
    status text not null default 'rascunho',
    destaque boolean not null default false,
    certificado_disponivel boolean not null default false,
    carga_horaria numeric(6,2),
    deleted_at timestamptz,
    constraint eventos_vagas_check check (vagas_total > 0 and vagas_ocupadas >= 0 and vagas_ocupadas <= vagas_total),
    constraint eventos_status_check check (status in ('rascunho','publicado','cancelado','encerrado','arquivado')),
    constraint eventos_formato_check check (formato in ('Presencial','Online','Híbrido'))
);

create table if not exists public.inscritos (
    id bigint generated by default as identity primary key,
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now(),
    user_id uuid references auth.users(id) on delete set null,
    evento_id bigint references public.eventos(id) on delete restrict,
    nome text not null,
    email text not null,
    telefone text not null,
    cidade text not null,
    evento text not null,
    tipo text not null,
    observacao text not null default '',
    status text not null default 'confirmado',
    ticket_code text not null default encode(gen_random_bytes(12), 'hex'),
    checked_in_at timestamptz,
    cancelled_at timestamptz,
    consentimento_lgpd boolean not null default false,
    constraint inscritos_status_check check (status in ('confirmado','lista_espera','cancelado','pendente')),
    constraint inscritos_ticket_unique unique (ticket_code)
