-- Remove políticas antigas para evitar que regras acadêmicas permissivas continuem ativas.
do $$
declare
    p record;
begin
    for p in
        select schemaname, tablename, policyname
        from pg_policies
        where schemaname = 'public'
          and tablename in ('profiles','eventos','inscritos','contatos','certificados','email_queue','audit_logs')
    loop
        execute format('drop policy if exists %I on %I.%I', p.policyname, p.schemaname, p.tablename);
    end loop;
end $$;

