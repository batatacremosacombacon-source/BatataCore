revoke all on public.profiles, public.eventos, public.inscritos, public.contatos,
    public.certificados, public.email_queue, public.audit_logs from anon, authenticated;

grant select on public.eventos to anon;
grant select, insert, update, delete on public.eventos to authenticated;
grant select, update on public.profiles to authenticated;
grant select, update, delete on public.inscritos to authenticated;
grant insert on public.contatos to anon;
grant select, insert, update, delete on public.contatos to authenticated;
grant select on public.certificados to authenticated;
grant select, insert, update, delete on public.email_queue to authenticated;
grant select on public.audit_logs to authenticated;
grant usage, select on all sequences in schema public to anon, authenticated;

